# Character Counter Program

# Description:
# This program counts the number of characters in a given string.

# How to Run:
# 1. Run the program.
# 2. Enter a string.
# 3. The program will display the character count.

# The code:

# Get a string from the user
text = input("Enter a string: ")

# Count the number of characters
character_count = len(text)

# Print the result
print(f"The number of characters in the string is: {character_count}")

# Number Reverser Program

# Description:
# This program reverses a given number.

# How to Run:
# 1. Run the program.
# 2. Enter a number.
# 3. The program will display the reversed number.

# The code:

# Get a number from the user
number = input("Enter a number: ")

# Reverse the number and print the result
reversed_number = number[::-1]
print(f"The reversed number is {reversed_number}.")
